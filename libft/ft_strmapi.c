#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*str;
	int		i;
	int		size_str;

	i = 0;
	if (!s || !f)
		return (0);
	size_str = ft_strlen(s);
	str = (char *)malloc(sizeof(char) * size_str + 1);
	if (!str)
		return (0);
	while (s[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
